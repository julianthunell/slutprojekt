public class movingObject {
    //x and y position of the object
    float x;
    float y;

    //size of the object
    int width;
    int height;
    public movingObject(int x, int y, int width, int height){
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
